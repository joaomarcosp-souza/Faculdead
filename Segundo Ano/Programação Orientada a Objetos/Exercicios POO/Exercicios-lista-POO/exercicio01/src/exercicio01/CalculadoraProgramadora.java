package exercicio01;

public class CalculadoraProgramadora extends Calculadora{

    public int calcularBitwiseAnd(int a, int b) {
        return a & b;
    }

    public int calcularBitwiseOr(int a, int b) {
        return a | b;
    }

    public int calcularBitwiseXor(int a, int b) {
        return a ^ b;
    }
}
