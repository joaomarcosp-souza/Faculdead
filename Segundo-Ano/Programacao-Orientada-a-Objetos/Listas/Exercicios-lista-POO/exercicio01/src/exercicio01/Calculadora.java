package exercicio01;

public class Calculadora {

    public int soma(int n1, int n2){
        return n1 + n2;
    };

    public int subtrair(int n1, int n2){
        return n1 - n2;
    };

    public int multiplicar(int n1, int n2) {
        return n1 * n2;
    };

    public int divisao(int n1, int n2) {
        return n1 / n2;
    };    
}
